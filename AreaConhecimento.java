public class AreaConhecimento{
   //atributos
   private int codigo;
   private String nome;
   
   //sets
   public void setCodigo(int codigo){
      this.codigo = codigo;
   }
   public void setNome(String nome){
      this.nome = nome;
   }
   //gets
   public int getCodigo(){
      return this.codigo;
   }
   public String getNome(){
      return this.nome;
   }
}
